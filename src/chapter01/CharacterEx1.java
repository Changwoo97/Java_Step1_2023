package chapter01;

public class CharacterEx1 {
	public static void main(String[] args) {
		var str1 = 'A';
		var str2 = "Hello, Java!";
		var str3 = "W";
		var str4 = "Welcome to Java!";
		
		System.out.println("str1 : " + str1);
		System.out.println("str2 : " + str2);
		System.out.println("str3 : " + str3);
		System.out.println("str4 : " + str4);
	}
}
