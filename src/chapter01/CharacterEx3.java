package chapter01;

public class CharacterEx3 {
	public static void main(String[] args) {
		int a = 65;
		System.out.println(a);
		System.out.println((char)a);
		System.out.println("==========");
		System.out.println();
		
		int b = 66;
		System.out.println(b);
		System.out.println((char)b);
		System.out.println("==========");
		System.out.println();
		
		int a2 = 97;
		System.out.println(a2);
		System.out.println((char)a2);
		System.out.println("==========");
	}
}
