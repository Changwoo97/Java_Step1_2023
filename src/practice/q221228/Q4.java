package practice.q221228;

public class Q4 {
	public static void main(String[] args) {
		int i = 10;
		double d = 2.5;
		
		System.out.println(i + " + " + d + " = " + (int)(i + d));
		System.out.println(i + " - " + d + " = " + (int)(i - d));
		System.out.println(i + " * " + d + " = " + (int)(i * d));
		System.out.println(i + " / " + d + " = " + (int)(i / d));
		System.out.println(i + " % " + d + " = " + (int)(i % d));
	}
}
